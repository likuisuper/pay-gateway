// 获取快手ios支付最接近的价格
func GetKsIosRecentlyPrice(originPrice int) int {
	if originPrice < 100 {
		return 100
	}

	// 一般不会达到这个上限
	if originPrice > 749900 {
		return 100
	}

	// 找到第一个价格大于等于originPrice的
	for _, tplPrice := range ks_ios_price_tpl {
		if tplPrice < originPrice {
			continue
		}

		return tplPrice
	}

	return 749900
}
